class Shiftperators
{
	public static void main(String args[])
	{
   				
		int x=10;
		int y=-10;
		System.out.println(x>>2);//division
   		System.out.println(x>>>2);//division ans same
		
		System.out.println(y>>2);//will return -3 it will round 2.5
		System.out.println(y>>>2);//will return msb of actual value i.e -3

		System.out.println(x<<2);// no three operators in lef
		System.out.println(y<<2);

		
		
		
		

		
	}

	
		
}
	